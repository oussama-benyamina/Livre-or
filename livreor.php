                <p>Kiyosaki divise les sources de revenus en 4 quadrants:</p>
                <ul>
                    <li><strong>Employé (E):</strong> Revenus provenant d'un emploi salarié en échange de temps.</li>
                    <li><strong>Travailleur indépendant (T):</strong> Revenus provenant d'un métier ou d'une profession libérale en échange de temps et d'efforts.</li>
                    <li><strong>Propriétaire d'entreprise (P):</strong> Revenus provenant d'un système d'affaires qui fonctionne sans votre présence constante.</li>
                    <li><strong>Investisseur (I):</strong> Revenus provenant d'investissements qui génèrent des flux de trésorerie passifs.</li>
                </ul>
                <p>L'objectif est de passer du côté gauche (E et T) au côté droit (P et I) pour atteindre la liberté financière.</p>
